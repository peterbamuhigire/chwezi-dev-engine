def write_generated(path: Path, content: str, *, force: bool) -> None:
    if path.exists():
        existing = path.read_text(encoding="utf-8", errors="replace")
        if GENERATED_MARKER not in existing and not force:
            raise ConfigError(f"Refusing to overwrite non-generated file: {path}")
        if existing == content:
            return
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content.replace("\r\n", "\n"), encoding="utf-8", newline="\n")


def generate_project_files(config: SuiteConfig, *, force: bool = False) -> list[Path]:
    config.generated_dir.mkdir(parents=True, exist_ok=True)
    outputs = {
        config.generated_dir / f"{config.product['slug']}.spec": spec_source(config),
        config.generated_dir / f"{config.product['slug']}.iss": inno_source(config),
        config.project_root / "scripts" / "build-desktop-suite.ps1": powershell_source(
            config
        ),
    }
    if not config.product.get("launcher_script"):
        outputs[config.generated_dir / "suite_launcher.py"] = launcher_source(config)
    if config.ci["enabled"]:
        outputs[config.project_root / config.ci["workflow_path"]] = workflow_source(
            config
        )
    for path, content in outputs.items():
        write_generated(path, content, force=force)
    hash_path = config.generated_dir / "manifest.sha256"
    hash_path.write_text(config.manifest_hash + "\n", encoding="ascii")
    paths = [*outputs, hash_path]
    for path in paths:
        print(f"Generated {path.relative_to(config.project_root)}")
    return paths


def scan_source(path: Path) -> list[tuple[str, str]]:
    try:
        text = path.read_text(encoding="utf-8")
    except UnicodeDecodeError:
        return [("warning", "entry script is not UTF-8; static packaging scan skipped")]
    findings: list[tuple[str, str]] = []
    if "shell=True" in text or "shell = True" in text:
        findings.append(
            ("error", "uses shell=True; replace with an argument array and shell=False")
        )
    if re.search(
        r"sys\.executable.{0,240}[\"'][^\"']+\.py[\"']",
        text,
        flags=re.DOTALL,
    ):
        findings.append(
            (
                "warning",
                "combines sys.executable with a .py path; frozen builds may relaunch themselves",
            )
        )
    if re.search(r"host\s*=\s*[\"']0\.0\.0\.0[\"']", text) and re.search(
        r"debug\s*=\s*True", text
    ):
        findings.append(
            (
                "error",
                "runs a debug web server on 0.0.0.0; bind local tools to 127.0.0.1 with debug off",
            )
        )
    if re.search(r"(?:pip|uv)\s+install", text) and (
        "subprocess" in text or "os.system" in text
    ):
        findings.append(
            (
                "warning",
                "appears to install dependencies at runtime; install them during the release build",
            )
        )
    return findings


def doctor(config: SuiteConfig, *, json_output: bool = False) -> int:
    checks: list[dict[str, str]] = []

    def add(level: str, subject: str, message: str) -> None:
        checks.append({"level": level, "subject": subject, "message": message})

    launcher_script = config.product.get("launcher_script")
    if launcher_script:
        launcher_path = config.project_root / launcher_script
        if launcher_path.is_file():
            add("pass", "launcher", f"existing launcher found: {launcher_script}")
            for level, message in scan_source(launcher_path):
                add(level, "launcher", message)
        else:
            add("error", "launcher", f"existing launcher not found: {launcher_script}")

    for app in config.applications:
        script_path = config.project_root / app.script
        if not script_path.is_file():
            add("error", app.app_id, f"entry script not found: {app.script}")
            continue
        add("pass", app.app_id, f"entry script found: {app.script}")
        for level, message in scan_source(script_path):
            add(level, app.app_id, message)

    for mapping in config.data:
        path = config.project_root / mapping.source
        add(
            "pass" if path.exists() else "error",
            "data",
            f"{mapping.source} -> {mapping.destination}: {'found' if path.exists() else 'missing'}",
        )

    icon = config.product.get("icon")
    if icon:
        path = config.project_root / icon
        add(
            "pass" if path.is_file() else "error",
            "icon",
            f"{icon}: {'found' if path.is_file() else 'missing'}",
        )

    if config.product["audience"] == "external":
        for field in ("license_file", "notices_file"):
            value = config.product.get(field)
            if not value:
                add(
                    "error",
                    "legal",
                    f"[product].{field} is required for external distribution",
                )
            else:
                path = config.project_root / value
                add(
                    "pass" if path.is_file() else "error",
                    "legal",
                    f"{value}: {'found' if path.is_file() else 'missing'}",
                )

    if shutil.which("uv"):
        add("pass", "tool", "uv found")
    else:
        add("error", "tool", "uv not found")
    if os.name == "nt":
        if shutil.which("powershell") or shutil.which("pwsh"):
            add("pass", "tool", "PowerShell found")
        else:
            add("error", "tool", "PowerShell not found")
        if config.build["installer"]:
            add(
                "pass" if find_iscc() else "warning",
                "tool",
                "Inno Setup compiler found"
                if find_iscc()
                else "Inno Setup compiler not found; installer build will fail",
            )
    else:
        add(
            "warning",
            "platform",
            "Windows artifacts must be built and tested on Windows",
        )

    errors = sum(item["level"] == "error" for item in checks)
    warnings = sum(item["level"] == "warning" for item in checks)
    if json_output:
        print(
            json.dumps(
                {"errors": errors, "warnings": warnings, "checks": checks}, indent=2
            )
        )
    else:
        for item in checks:
            print(f"{item['level'].upper():7} {item['subject']}: {item['message']}")
        print(f"Doctor result: {errors} error(s), {warnings} warning(s)")
    return 1 if errors else 0


def find_iscc() -> str | None:
    direct = shutil.which("ISCC.exe") or shutil.which("iscc")
    if direct:
        return direct
    if os.name != "nt":
        return None
    roots = [os.environ.get("PROGRAMFILES(X86)"), os.environ.get("PROGRAMFILES")]
    for root in filter(None, roots):
        for version in ("Inno Setup 7", "Inno Setup 6"):
            candidate = Path(root) / version / "ISCC.exe"
            if candidate.is_file():
                return str(candidate)
    return None


def run_build(args: argparse.Namespace) -> int:
    config = load_config(args.config)
    generate_project_files(config, force=args.force_generated)
    shell = shutil.which("pwsh") or shutil.which("powershell")
    if not shell:
        raise ConfigError("PowerShell is required to run the generated Windows build")
    command = [
        shell,
        "-NoProfile",
        "-ExecutionPolicy",
        "Bypass",
        "-File",
        str(config.project_root / "scripts" / "build-desktop-suite.ps1"),
    ]
    if args.unsigned_development_build:
        command.append("-UnsignedDevelopmentBuild")
    if args.skip_tests:
        command.append("-SkipTests")
    if args.skip_installer:
        command.append("-SkipInstaller")
    completed = subprocess.run(command, cwd=config.project_root, check=False)  # noqa: S603
    return completed.returncode


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        description="Generate reproducible PyInstaller desktop-suite release files."
    )
    subparsers = parser.add_subparsers(dest="command", required=True)

    init = subparsers.add_parser("init", help="Create packaging/desktop-suite.toml")
    init.add_argument("--project-root", type=Path, required=True)
    init.add_argument("--product-name", required=True)
    init.add_argument("--publisher", required=True)
    init.add_argument("--version", default="0.1.0")
    init.add_argument("--slug")
    init.add_argument(
        "--audience", choices=("internal", "external"), default="external"
    )
    init.add_argument("--app", action="append", required=True, type=parse_app_argument)
    init.add_argument("--force", action="store_true")
    init.set_defaults(func=init_manifest)

    generate = subparsers.add_parser(
        "generate", help="Generate project-local release files"
    )
    generate.add_argument("--config", type=Path, required=True)
    generate.add_argument(
        "--force", action="store_true", help="Replace conflicting generated targets"
    )
    generate.set_defaults(
        func=lambda args: (
            generate_project_files(load_config(args.config), force=args.force) and 0
        )
    )

    check = subparsers.add_parser(
        "doctor", help="Validate manifest, source risks, and build tools"
    )
    check.add_argument("--config", type=Path, required=True)
    check.add_argument("--json", action="store_true")
    check.set_defaults(
        func=lambda args: doctor(load_config(args.config), json_output=args.json)
    )

    build = subparsers.add_parser(
        "build", help="Generate files and run the Windows build"
    )
    build.add_argument("--config", type=Path, required=True)
    build.add_argument("--unsigned-development-build", action="store_true")
    build.add_argument("--skip-tests", action="store_true")
    build.add_argument("--skip-installer", action="store_true")
    build.add_argument("--force-generated", action="store_true")
    build.set_defaults(func=run_build)
    return parser


def main(argv: Sequence[str] | None = None) -> int:
    parser = build_parser()
    args = parser.parse_args(argv)
    try:
        return int(args.func(args))
    except ConfigError as exc:
        parser.error(str(exc))
    return 2


if __name__ == "__main__":
    raise SystemExit(main())
